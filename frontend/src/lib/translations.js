export const getTranslation = (lang, path) => {
  const keys = path.split(".");
  let result = translations[lang];
  for (const key of keys) {
    if (result && result[key]) {
      result = result[key];
    } else {
      // Fallback to Turkish
      result = translations["tr"];
      for (const k of keys) {
        if (result && result[k]) {
          result = result[k];
        } else {
          return path;
        }
      }
      return result;
    }
  }
  return result;
};
